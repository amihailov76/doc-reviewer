ST = make_styles()

# ─── Вспомогательные блоки ────────────────────────────────────────────────────
def HR():
    return HRFlowable(width="100%", thickness=0.5, color=GRAY_MED, spaceAfter=8, spaceBefore=4)

def SP(h=4):
    return Spacer(1, h * mm)

def p(text, style="body"):
    return Paragraph(text, ST[style])

def h1(text):
    return p(text, "h1")

def h2(text):
    return p(text, "h2")

def note(label, text):
    """Информационный блок с синей границей слева."""
    inner = Table(
        [[Paragraph(f'<b>{label}</b> {text}', ST["note"])]],
        colWidths=[CONTENT_W - 8],
    )
    inner.setStyle(TableStyle([
        ("BACKGROUND", (0, 0), (-1, -1), NOTE_BG),
        ("LEFTPADDING",  (0, 0), (-1, -1), 8),
        ("RIGHTPADDING", (0, 0), (-1, -1), 8),
        ("TOPPADDING",   (0, 0), (-1, -1), 6),
        ("BOTTOMPADDING",(0, 0), (-1, -1), 6),
    ]))
    outer = Table(
        [[None, inner]],
        colWidths=[3, CONTENT_W - 3],
    )
    outer.setStyle(TableStyle([
        ("BACKGROUND",   (0, 0), (0, -1), NOTE_BORDER),
        ("LEFTPADDING",  (0, 0), (-1, -1), 0),
        ("RIGHTPADDING", (0, 0), (-1, -1), 0),
        ("TOPPADDING",   (0, 0), (-1, -1), 0),
        ("BOTTOMPADDING",(0, 0), (-1, -1), 0),
        ("VALIGN",       (0, 0), (-1, -1), "MIDDLE"),
    ]))
    return KeepTogether([SP(1), outer, SP(2)])

def warn(text):
    """Предупреждающий блок с красной границей слева."""
    inner = Table(
        [[Paragraph(f'<b>Важно!</b> {text}', ST["note"])]],
        colWidths=[CONTENT_W - 8],
    )
    inner.setStyle(TableStyle([
        ("BACKGROUND", (0, 0), (-1, -1), WARN_BG),
        ("LEFTPADDING",  (0, 0), (-1, -1), 8),
        ("RIGHTPADDING", (0, 0), (-1, -1), 8),
        ("TOPPADDING",   (0, 0), (-1, -1), 6),
        ("BOTTOMPADDING",(0, 0), (-1, -1), 6),
    ]))
    outer = Table(
        [[None, inner]],
        colWidths=[3, CONTENT_W - 3],
    )
    outer.setStyle(TableStyle([
        ("BACKGROUND",   (0, 0), (0, -1), WARN_BORDER),
        ("LEFTPADDING",  (0, 0), (-1, -1), 0),
        ("RIGHTPADDING", (0, 0), (-1, -1), 0),
        ("TOPPADDING",   (0, 0), (-1, -1), 0),
        ("BOTTOMPADDING",(0, 0), (-1, -1), 0),
        ("VALIGN",       (0, 0), (-1, -1), "MIDDLE"),
    ]))
    return KeepTogether([SP(1), outer, SP(2)])

def bullets(items):
    elems = []
    for item in items:
        elems.append(Paragraph(f"• {item}", ST["bullet"]))
    return elems

def numbered(items, start=1):
    elems = []
    for i, item in enumerate(items, start):
        elems.append(Paragraph(f"{i}. {item}", ST["bullet"]))
    return elems

def code_block(text):
    return KeepTogether([
        SP(1),
        Paragraph(text, ST["code"]),
        SP(1),
    ])

def simple_table(headers, rows, col_widths=None):
    if col_widths is None:
        n = len(headers)
        col_widths = [CONTENT_W / n] * n

    data = [[Paragraph(h, ST["th"]) for h in headers]]
    for row in rows:
        data.append([Paragraph(cell, ST["td"]) for cell in row])

    tbl = Table(data, colWidths=col_widths, repeatRows=1)
    style = [
        ("BACKGROUND",   (0, 0), (-1, 0), GRAY_LIGHT),
        ("GRID",         (0, 0), (-1, -1), 0.5, GRAY_MED),
        ("FONTNAME",     (0, 0), (-1, 0), FONT_BOLD),
        ("LEFTPADDING",  (0, 0), (-1, -1), 6),
        ("RIGHTPADDING", (0, 0), (-1, -1), 6),
        ("TOPPADDING",   (0, 0), (-1, -1), 5),
        ("BOTTOMPADDING",(0, 0), (-1, -1), 5),
        ("VALIGN",       (0, 0), (-1, -1), "TOP"),
    ]
    tbl.setStyle(TableStyle(style))
    return KeepTogether([SP(2), tbl, SP(2)])

def two_col_table(rows, col_widths=None):
    """Таблица без заголовка с двумя колонками."""
    if col_widths is None:
        col_widths = [CONTENT_W * 0.33, CONTENT_W * 0.67]
    data = []
    for left, right in rows:
        data.append([Paragraph(left, ST["th"]), Paragraph(right, ST["td"])])
    tbl = Table(data, colWidths=col_widths)
    tbl.setStyle(TableStyle([
        ("GRID",         (0, 0), (-1, -1), 0.5, GRAY_MED),
        ("BACKGROUND",   (0, 0), (0, -1), GRAY_LIGHT),
        ("LEFTPADDING",  (0, 0), (-1, -1), 6),
        ("RIGHTPADDING", (0, 0), (-1, -1), 6),
        ("TOPPADDING",   (0, 0), (-1, -1), 5),
        ("BOTTOMPADDING",(0, 0), (-1, -1), 5),
        ("VALIGN",       (0, 0), (-1, -1), "TOP"),
    ]))
    return KeepTogether([SP(2), tbl, SP(2)])

# ─── Колонтитул ───────────────────────────────────────────────────────────────
def footer(canvas, doc):
    canvas.saveState()
    canvas.setFont(FONT_REGULAR, 8)
    canvas.setFillColor(colors.HexColor("#95A5A6"))
    txt = "doc-reviewer · Руководство пользователя · Версия 1.1"
    canvas.drawCentredString(W / 2, 12 * mm, txt)
    canvas.setStrokeColor(GRAY_MED)
    canvas.setLineWidth(0.5)
    canvas.line(MARGIN, 16 * mm, W - MARGIN, 16 * mm)
    canvas.restoreState()

